    private void adicionarLinha(Bicicleta bicicleta) {
        modeloAlterar.addRow(
                new Object[]{bicicleta.getCodigo(), bicicleta.getDescricao(), bicicleta.getAno(),
                    bicicleta.getCor(), bicicleta.getPneu(), bicicleta.getFabricante().getCnpj(), getFabricanteNome(bicicleta.getFabricante()),
                    bicicleta.getCategoria().getCodigo(), bicicleta.getCategoria().getDescricao()}
        );
    }

    private void carregarTabela() {
        modeloAlterar.setRowCount(0);
        try {
            bicicletas = fachada.listar(new Bicicleta());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        for (Bicicleta b : bicicletas) {
            adicionarLinha(b);
        }
        jTable1.setModel(modeloAlterar);
    }

    private String getFabricanteNome(Fabricante fabricante) {
        String nome = fabricante.getRazaoSocial();
        if (!fabricante.getNomeFantasia().equals("")) {
            nome += " / " + fabricante.getNomeFantasia();
        }
        return nome;
    }

    private Bicicleta getValues() throws Exception {
        if (selectedRow < 0) {
            throw new Exception("Nenhuma linha selecionada.");
        }
        Bicicleta bicicleta = new Bicicleta();
        //"Cód. Bicicleta_0", "Nome_1", "Ano_2", "Cor_3", "Pneu_4", "CNPJ_5", "Fabricante_6", "Cód. Categoria_7", "Categoria_8"
        bicicleta.setCodigo(Integer.parseInt(jTable1.getValueAt(selectedRow, 0) + ""));
        bicicleta.setDescricao(jTable1.getValueAt(selectedRow, 1) + "");
        bicicleta.setAno(jTable1.getValueAt(selectedRow, 2) + "");
        bicicleta.setCor(jTable1.getValueAt(selectedRow, 3) + "");
        bicicleta.setPneu(jTable1.getValueAt(selectedRow, 4) + "");

        Fabricante fabricante = new Fabricante();
        fabricante.setCnpj(jTable1.getValueAt(selectedRow, 5) + "");
        bicicleta.setFabricante(fabricante);

        Categoria categoria = new Categoria();
        categoria.setCodigo(Integer.parseInt(jTable1.getValueAt(selectedRow, 7) + ""));
        bicicleta.setCategoria(categoria);

        return bicicleta;
    }

    private int findFabricante(Bicicleta b) {
        String cpnj = b.getFabricante().getCnpj();
        int found = -1;

        for (int i = 0; i < fabricantes.size(); i++) {
            String cnpjFound = fabricantes.get(i).getCnpj();
            if (cnpjFound.equals(cpnj)) {
                found = i;
                break;
            }
        }

        return found;
    }

    private int findCategoria(Bicicleta b) {
        int codigo = b.getCategoria().getCodigo();
        int found = -1;

        for (int i = 0; i < categorias.size(); i++) {
            int codigoFound = categorias.get(i).getCodigo();
            if (codigoFound == codigo) {
                found = i;
                break;
            }
        }
        return found;
    }

    private void carregarFabricantes() {
        modeloFabricante.removeAllElements();
        modeloFabricanteEdit.removeAllElements();
        try {
            fabricantes = fachada.listar(new Fabricante());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        for (Fabricante f : fabricantes) {
            String nome = getFabricanteNome(f);
            modeloFabricante.addElement(nome);
            modeloFabricanteEdit.addElement(nome);
        }
        jCBoxFab.setModel(modeloFabricante);
        jCBoxFabricanteEdit.setModel(modeloFabricanteEdit);
    }

    private void carregarCategorias() {
        modeloCategoria.removeAllElements();
        modeloCategoriaEdit.removeAllElements();
        try {
            categorias = fachada.listar(new Categoria());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        for (Categoria c : categorias) {
            modeloCategoria.addElement(c.getDescricao());
            modeloCategoriaEdit.addElement(c.getDescricao());
        }
        jCBoxCat.setModel(modeloCategoria);
        jCBoxCategoriaEdit.setModel(modeloCategoriaEdit);
    }

    private void atualizarLinha(Bicicleta bicicleta) {
        if (bicicleta.getFabricante().getNomeFantasia() == null) {
            bicicleta.getFabricante().setNomeFantasia("");
        }
        //"Cód. Bicicleta_0", "Nome_1", "Ano_2", "Cor_3", "Pneu_4", "CNPJ_5", "Fabricante_6", "Cód. Categoria_7", "Categoria_8"
        jTable1.setValueAt(bicicleta.getDescricao(), selectedRow, 1);
        jTable1.setValueAt(bicicleta.getAno(), selectedRow, 2);
        jTable1.setValueAt(bicicleta.getCor(), selectedRow, 3);
        jTable1.setValueAt(bicicleta.getPneu(), selectedRow, 4);
        jTable1.setValueAt(bicicleta.getFabricante().getCnpj(), selectedRow, 5);
        jTable1.setValueAt(getFabricanteNome(bicicleta.getFabricante()), selectedRow, 6);
        jTable1.setValueAt(bicicleta.getCategoria().getCodigo(), selectedRow, 7);
        jTable1.setValueAt(bicicleta.getCategoria().getDescricao(), selectedRow, 8);
    }

    private void carregarComponentes() {
        carregarFabricantes();
        carregarCategorias();
        carregarTabela();
        selectedRow = -1;
    }

