    private void jBtnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCadastrarActionPerformed
        // TODO add your handling code here:
        try {
            Bicicleta bicicleta = new Bicicleta();
            bicicleta.setCodigo(Integer.parseInt(jTextFieldCod.getText()));
            bicicleta.setDescricao(jTextFieldNome.getText());
            bicicleta.setAno(jFTextFieldAno.getText());
            bicicleta.setCor(jTextFieldCor.getText());
            bicicleta.setPneu(jTextFieldPneu.getText());
            bicicleta.setFabricante(fabricantes.get(jCBoxFab.getSelectedIndex()));
            bicicleta.setCategoria(categorias.get(jCBoxCat.getSelectedIndex()));

            fachada.inserir(bicicleta);
            JOptionPane.showMessageDialog(this, "Bicicleta inserida com sucesso.");

            adicionarLinha(bicicleta);
            resetarCampos();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jBtnCadastrarActionPerformed

