    private void colocarFabricates() {
        Fachada fachada = new Fachada();
        try {
            fabricantes = fachada.listar(new Fabricante());
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
        
        for (Fabricante f : fabricantes) {
            String nome;
            if (f.getNomeFantasia() == null) {
                nome = f.getRazaoSocial();
            } else {
                nome = f.getNomeFantasia();
            }
            
            modeloFabricante.addElement(nome);
        }
        
        jComboBoxFabricante.setModel(modeloFabricante);
    }
    
    private void colocarCategorias() {
        Fachada fachada = new Fachada();
        try {
            categorias = fachada.listar(new Categoria());
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
        
        for (Categoria c : categorias) {
            modeloCategoria.addElement(c.getDescricao());
        }
        
        jComboBoxCategoria.setModel(modeloCategoria);
    }

