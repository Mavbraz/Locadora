    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        Fachada fachada = new Fachada();
        Bicicleta bicicleta = new Bicicleta();
        bicicleta.setCodigo(Integer.parseInt(jFormattedTextFieldCodigo.getText()));
        bicicleta.setFabricante(fabricantes.get(jComboBoxFabricante.getSelectedIndex()));
        bicicleta.setCategoria(categorias.get(jComboBoxCategoria.getSelectedIndex()));
        bicicleta.setDescricao(jTextFieldNome.getText());
        bicicleta.setCor(jTextFieldCor.getText());
        bicicleta.setPneu(jTextFieldPneu.getText());
        bicicleta.setAno(jFormattedTextFieldAno.getText());

        try {
            fachada.inserir(bicicleta);
            JOptionPane.showMessageDialog(this, "Bicicleta cadastrada com sucesso.");
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

