    private void resetarCampos() {
        jFormattedTextFieldCod.setText("0");
        jTextFieldNome.setText("");
        jFormattedTextFieldAno.setText("");
        jTextFieldCor.setText("");
        jTextFieldPneu.setText("");
        jComboBoxFab.setSelectedIndex(0);
        jComboBoxCat.setSelectedIndex(0);
        jFormattedTextFieldCod.requestFocus();
    }

    private void adicionarLinha(Bicicleta bicicleta) {
        String fabricante = getFabricanteNome(bicicleta.getFabricante());

        modeloAlterar.addRow(
                new Object[]{bicicleta.getCodigo(), bicicleta.getDescricao(), bicicleta.getAno(),
                    bicicleta.getCor(), bicicleta.getPneu(), fabricante, bicicleta.getCategoria().getDescricao()}
        );
    }

    private void carregarTabela() {
        try {
            bicicletas = fachada.listar(new Bicicleta());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        for (Bicicleta b : bicicletas) {
            adicionarLinha(b);
        }
        jTable1.setModel(modeloAlterar);
    }

    private String getFabricanteNome(Fabricante fabricante) {
        String nome;
        if (fabricante.getNomeFantasia() != null) {
            nome = fabricante.getNomeFantasia();
        } else {
            nome = fabricante.getRazaoSocial();
        }
        return nome;
    }

    private void carregarFabricantes() {
        try {
            fabricantes = fachada.listar(new Fabricante());
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }

        for (Fabricante f : fabricantes) {
            String nome = getFabricanteNome(f);
            modeloFabricante.addElement(nome);
        }

        jComboBoxFab.setModel(modeloFabricante);
    }

    private void carregarCategorias() {
        try {
            categorias = fachada.listar(new Categoria());
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }

        for (Categoria c : categorias) {
            modeloCategoria.addElement(c.getDescricao());
        }

        jComboBoxCat.setModel(modeloCategoria);
    }
