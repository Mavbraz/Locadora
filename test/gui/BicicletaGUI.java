    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        // TODO add your handling code here:
        try {
            Bicicleta bicicleta = new Bicicleta();
            bicicleta.setCodigo(Integer.parseInt(jFormattedTextFieldCod.getText()));
            bicicleta.setDescricao(jTextFieldNome.getText());
            bicicleta.setAno(jFormattedTextFieldAno.getText());
            bicicleta.setCor(jTextFieldCor.getText());
            bicicleta.setPneu(jTextFieldPneu.getText());
            bicicleta.setFabricante(fabricantes.get(jComboBoxFab.getSelectedIndex()));
            bicicleta.setCategoria(categorias.get(jComboBoxCat.getSelectedIndex()));
            fachada.inserir(bicicleta);
            JOptionPane.showMessageDialog(this, "Fabricante inserido com sucesso.");
            adicionarLinha(bicicleta);
            resetarCampos();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

